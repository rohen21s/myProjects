    public void emplenarTaula() {
        Object columnNames[] = {"ID", "Nom", "Codi", "Contacte", "Estat", "Tipus"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        for (int i = 0; i < LlistaEntitats.llistaEntitat.size(); i++) {

            Entitat e = LlistaEntitats.returnList().get(i);
            if(e instanceof Empresa){
                Object rowData[] = {e.getId(), e.getName(), e.getCode(), e.getContact(), e.getStatus(), "Empresa"};
                model.addRow(rowData);
            }else{
                Object rowData[] = {e.getId(), e.getName(), e.getCode(), e.getContact(), e.getStatus(), "Institut"};
                model.addRow(rowData);  
            }
        }
        jTable1.setModel(model);
    }
    public void consulta(String filter) {

        Object columnNames[] = {"ID", "Nom", "Codi", "Contact", "Status", "Tipus"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);
        
        for (int i = 0; i < LlistaEntitats.llistaEntitat.size(); i++) {
            
            Entitat e = LlistaEntitats.returnList().get(i);
            String x = e.toString();

            if (x.toLowerCase().contains(filter.toLowerCase())) {
                Object rowData[] = {e.getId(), e.getName(), e.getCode(), e.getContact() , e.getStatus()};
                model.addRow(rowData);
            }
        }
        jTable1.setModel(model);

    }

